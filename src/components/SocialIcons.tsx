// Helper function to get the right icon for a social platform
export const getSocialIcon = (platform: string, size?: number, className?: string) => {
  switch (platform.toLowerCase()) {
    case 'discord':
      return <DiscordIcon size={size} className={className} />;
    case 'roblox':
      return <RobloxIcon size={size} className={className} />;
    case 'spotify':
      return <SpotifyIcon size={size} className={className} />;
    case 'telegram':
      return <TelegramIcon size={size} className={className} />;
    case 'twitter':
    case 'x':
      return <TwitterIcon size={size} className={className} />;
    case 'github':
      return <GitHubIcon size={size} className={className} />;
    default:
      return null;
  }
};
